// Comparação de igualdade
console.log(5 == '5'); // true, compara apenas o valor

// Comparação de igualdade estrita
console.log(5 === '5'); // false, compara valor e tipo

// Comparação de desigualdade
console.log(5 != '5'); // false, compara apenas o valor

// Comparação de desigualdade estrita
console.log(5 !== '5'); // true, compara valor e tipo

// Comparação maior que
console.log(10 > 5); // true

// Comparação menor que
console.log(10 < 5); // false

// Comparação maior ou igual a
console.log(10 >= 10); // true

// Comparação menor ou igual a
console.log(10 <= 5); // false